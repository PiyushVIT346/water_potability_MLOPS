def greet(name):
    """Prints a greeting message."""
    print(f"Hello {name}")


def add(a, b):
    """Returns the sum of two numbers."""
    return a + b


def mul(a, b):
    """Returns the product of two numbers."""
    return a * b


greet("Anil")
print(add(2, 3))
print(mul(2, 3))

